void screen_symbol(char *symbol)
{	// В качестве параметра - ссылка на char. Это массив, 
	// первый элемент - значение pages, ширины массива, 
	// второй - длина массива, остальные - сами данные символа.
	
	char pages = pgm_read_byte(symbol++);
	char clmns = pgm_read_byte(symbol++);
		
	i2c_start(SSD1306_I2C_ADDRESS);
	i2c_write(COM);
	i2c_write(SSD1306_MEMORYMODE);
	i2c_write(0x01);
	i2c_write(0x22);
	i2c_write(0);
	i2c_write(pages - 1);
	i2c_stop();
	
	i2c_start(SSD1306_I2C_ADDRESS);
	i2c_write(DATA);//data mode
	for (int i = 0, lenght = pages*clmns; i < lenght; i++)
	{
		i2c_write(pgm_read_byte(symbol++));
		if (i == 128 * 4 - 1)
		{
			i2c_stop();
			i2c_start(SSD1306_I2C_ADDRESS);
			i2c_write(COM);
			i2c_write(SSD1306_MEMORYMODE);
			i2c_write(0x01);
			i2c_write(0x22);
			i2c_write(pages);
			i2c_write(pages*2 - 1);
			i2c_stop();
			i2c_start(SSD1306_I2C_ADDRESS);
			i2c_write(DATA);
		}
	}
	i2c_stop();
}

