void screen_string(char *string)
{
	i2c_start(SSD1306_I2C_ADDRESS);
	i2c_write(DATA);
	while(*string != '\0')
	{
		for (char i = 0; i < 5; i++)
		{
			i2c_write(pgm_read_byte(&screen_buff[*string*5 + i]));
		}
		i2c_write(0x00);
		string++;
	}
	i2c_stop();
}

void screen_sizechar(unsigned char symbol, unsigned char size)
{
	i2c_start(SSD1306_I2C_ADDRESS);
	i2c_write(COM);
	i2c_write(SSD1306_MEMORYMODE);
	i2c_write(0x01);
	i2c_write(0x22);
	i2c_write(0);
	i2c_write(size - 1);
	i2c_stop();
	
	i2c_start(SSD1306_I2C_ADDRESS);
	i2c_write(DATA);//data mode
	
	if (symbol == ' ')
	{
		for (char i = 0; i < size*size*5; i++) i2c_write(0);
		return;
	}
	
	for (char x=0; x<5; x++)
	{
		unsigned char in_line = pgm_read_byte(&screen_buff[symbol*5+x]);
		if (in_line == 0) 
		{
			for (int i = 0; i < size*size; i++) i2c_write(0);
			continue;
		}
		
		unsigned char out_line[size];
		for (unsigned char i = 0; i < size; i++) out_line[i] = 0;
		unsigned char out_index = 0;
		for (int out_mask = 1, in_mask = 1; in_mask <= 0x80; in_mask <<= 1)
		{
			for (int i = 0; i < size; i++)
			{
				out_line[out_index] |= in_line & in_mask ? out_mask : 0;
				
				if (out_mask < 0x80) out_mask <<= 1;
				else
				{
					out_mask = 1;
					out_index++;
				}
				
			}
			
		}
		for (int i = 0; i < size*size; i++) i2c_write(out_line[i % size]);
	}
	for(int i = 0; i < size*size; i++)	  //пробел в size точек между символами
	{
		i2c_write(0x00);
	}
	i2c_stop();    // stop transmitting
	
	/*screen_clmn += 11;
	if(screen_clmn>SSD1306_LCDWIDTH)
	{
		screen_clmn = SSD1306_DEFAULT_SPACE;
	}*/
}



